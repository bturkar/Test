"""*************isalnum()***************************************************"""
print('----------isalnum()-----------------')
print('isalnum() : returns true if string is alphanumeric')

s1 = 'bhaskar'
s2 = 'ME11123'
s3 = '1235'
s4 = '1'
s5 = '&^%$#'
print('bhaskar',s1.isalnum())
print('ME11123', s2.isalnum())
print('1235', s3.isalnum())
print('1', s4.isalnum())
print('&^%$#', s5.isalnum())
print('--------------------------------------------------------------------------------------------')
