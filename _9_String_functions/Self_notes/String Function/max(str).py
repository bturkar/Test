"""**max(str) : Returns the max alphabetical character from the string str.(Using ASCII Values)***"""
s1 = "Bhaskar Turkar"
s2 = "Z z "
s3 = "#$$#@"
print("------------- 24. max(str) -----------------")
print('max(str) : Returns the max alphabetical character from the string str.(Using ASCII Values)')
print("max of (Bhaskar Turkar) is         :", max(s1))
print("max of (Z z)    :", max(s2))
print("max of (#$$#@)    :", max(s3))
print("-------------------------------------------------------------------------------------")
