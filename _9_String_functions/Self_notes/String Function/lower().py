"""*****lower() : converts string to all lowercase characters****"""
str1 = "BHASKAR TURKAR"
str2 = "Bhaskar Turkar"
print("------------- lower() -----------------")
print('lower() : converts string to all lowercase characters')
print(" 'BHASKAR TURKAR' in lower characters is  :", "BHASKAR TURKAR".lower())
print(" 'BHASKAR TURKAR' in lower characters is  :", str1.lower())

print("'Bhaskar Turkar' in lower is :", str2.lower())
print("-------------------------------------------------------------------------------------")
