"""**min(str) : Returns the min alphabetical character from the string str.(Using ASCII Values)***"""
s1 = "Bhaskar Turkar"
s2 = "Z z "
s3 = "#$$#@"
print("------------- 24. min(str) -----------------")
print('min(str) : Returns the min alphabetical character from the string str.(Using ASCII Values)')
print("min of (Bhaskar Turkar) is         :", min(s1))
print("min of (Z z)    :", min(s2))
print("min of (#$$#@)    :", min(s3))
print("-------------------------------------------------------------------------------------")
