"""*****swapcase() : Inverts case for all letters in string.***********"""
s1 = "hello world"
s2 = 'HELLO WORLD'
s3 = 'HellO WorlD'
print("------------- swapcase() -----------------")
print('swapcase() : swaps case')
print("Swaping the case of  'hello world':", s1.swapcase())
print("Swaping the case of  'HELLO WORLD':", s2.swapcase())
print("Swaping the case of  'HellO WorlD':", s3.swapcase())
print("-------------------------------------------------------------------------------------")

