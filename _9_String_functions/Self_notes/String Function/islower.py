"""*************islower()***************************************************"""
print('----------islower()-----------------')
print('islower() : returns true if all substrings are in lower case')
s1 = 'bhaskar'
s2 = 'MEchanical'
s3 = '1235'
s4 = 'Bhaskar Turkar'
s5 = 'Bhaskar'
s6 = 'bHASKAR tURKAR'
print("bhaskar", s1.islower())
print('ME11123', s2.islower())
print('1235', s3.islower())
print('1', s4.islower())
print('&^%$#', s5.islower())
print('bHASKAR tURKAR',s6.islower())
print('--------------------------------------------------------------------------------------------')
