"""**************ljust()*********************************"""
str1 = "bhaskar"
print("------------- ljust() -----------------")
print('ljust() : adjust length to mentioned number with provided char on right')
print("The string is            :", str1)
print("The string is            :", str1.ljust(10))
print("ljust(10,*) of ake is    :", str1.ljust(10, "*"))
print("-------------------------------------------------------------------------------------")
