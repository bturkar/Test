
"""*************isalpha()***************************************************"""
print('----------isalpha()-----------------')
print('isalpha() : returns true if all substrings are alphabets')
s1 = 'bhaskar'
s2 = 'ME11123'
s3 = '1235'
s4 = '1'
s5 = '&^%$#'
print("bhaskar", s1.isalpha())
print('ME11123', s2.isalpha())
print('1235', s3.isalpha())
print('1', s4.isalpha())
print('&^%$#', s5.isalpha())
