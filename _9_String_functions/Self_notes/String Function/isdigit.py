"""*************isdigit()***************************************************"""
print('----------isdigit()-----------------')
print('isdigit() : returns true if all substrings are digit')
s1 = 'bhaskar'
s2 = 'ME11123'
s3 = '1235'
s4 = '1'
s5 = '&^%$#'
print("bhaskar", s1.isdigit())
print('ME11123', s2.isdigit())
print('1235', s3.isdigit())
print('1', s4.isdigit())
print('&^%$#', s5.isdigit())
print('--------------------------------------------------------------------------------------------')
