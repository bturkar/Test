"""*************islower()***************************************************"""
print('----------islower()-----------------')
print('islower() : returns true if all substrings are in upper case')
s1 = 'BHASKAR'
s2 = 'MEchanical'
s3 = '1235'
s4 = 'Bhaskar Turkar'
s5 = 'Bhaskar'
s6 = 'bHASKAR tURKAR'
print("bhaskar", s1.isupper())
print('ME11123', s2.isupper())
print('1235', s3.isupper())
print('1', s4.isupper())
print('&^%$#', s5.isupper())
print('bHASKAR tURKAR', s6.isupper())
print('--------------------------------------------------------------------------------------------')
