"""*******strip() : removes mentioned char (left)********"""
str1 = "    bhaskar turkar  "
str2 = "***bhaskar turkar**"
print("-------------lstrip() -----------------")
print('lstrip() : removes mentioned char (left)')
print("lstrip of  '    bhaskar turkar  ':", str1.lstrip(" "))
print("lstrip of * in  ***bhaskar turkar**    :", str2.lstrip("*"))
print("-------------------------------------------------------------------------------------")
