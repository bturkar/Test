"""*******************len() : returns length***********************"""
s1 = 'bhaskar turkar'
s2 = 'bhaskarturkar'
s3 = 'Bhaskar    turkar'
s4 = '     bhaskar  turkar          '
print("------------- len() -----------------")
print("len() : returns length")
print("Length of 'bhaskar turkar' :", len(s1))
print("Length of 'bhaskarturkar' :", len(s2))
print("Length of 'bhaskar    turkar' :", len(s3))
print("Length of '       bhaskar  turkar         ' :", len(s4))
print("-------------------------------------------------------------------------------------")
