"""********** istitle() : returns true if string is in titlecase******"""
str1 = "Bhaskar Turkar"
str2 = "Bhaskar turkar"
str3 = "BHASKAR TURKAR"
print("------------- istitle() -----------------")
print('istitle() : returns true if string is in titlecase')
print("Bhaskar Turkar is title    :", str1.istitle())
print("Bhaskar turkar is title    :", str2.istitle())
print("BHASKAR TURKAR is title    :", str3.istitle())

