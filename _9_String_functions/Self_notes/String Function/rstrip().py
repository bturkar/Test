"""*******rstrip() : removes mentioned char (right)********"""
str1 = "    bhaskar turkar  "
str2 = "***bhaskar turkar**"
print("-------------rstrip() -----------------")
print('rstrip() : removes mentioned char (left)')
print("rstrip of  '    bhaskar turkar  ':", str1.rstrip(" "))
print("rstrip of * in  ***bhaskar turkar**    :", str2.rstrip("*"))
print("-------------------------------------------------------------------------------------")
