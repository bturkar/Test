"""**************rjust()*********************************"""
str1 = "bhaskar"
print("------------- rjust() -----------------")
print('rjust() : adjust length to mentioned number with provided char on left')
print("The string is            :", str1)
print("The string is            :", str1.rjust(10))
print("rjust(10,*) of ake is    :", str1.rjust(10, "*"))
print("-------------------------------------------------------------------------------------")
