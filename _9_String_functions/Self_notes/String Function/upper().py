"""***upper() : converts string to all uppercase characters****"""
s1 = 'bhaskar turkar'
s2 = "Bhaskar Turkar"
s3 = "BHASKAR TURKAR"
print("------------- upper() -----------------")
print('upper() : converts string to all uppercase characters')
print(f'{s1} in upper characters is  :', "bhaskar turkar".upper())
print(f'{s1} in upper characters is  :', s1.upper())
print(f'{s2} in upper characters is  :', "Bhaskar Turkar".upper())
print(f'{s2} in upper characters is  :', s2.upper())
print(f'{s3} in upper characters is  :', "BHASKAR TURKAR".upper())
print(f'{s3} in upper characters is  :', s3.upper())


