"""**********************isnumeric()***********************"""
"""isnumeric() : returns true if all characters in the string are numeric"""
str1 = "123546"
str2 = u"16041991"
str3 = "me11123"
print("------------- isnumeric() -----------------")
print('isnumeric() : returns true if all characters in the string are numeric')
print("123546 is numeric   :", str1.isnumeric())
print('u"16041991" is numeric    :', str2.isnumeric())
print("me11123 is numeric   :", str3.isnumeric())
print("-------------------------------------------------------------------------------------")
